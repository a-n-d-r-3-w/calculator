define({
    add: function (operand1, operand2) {
        return operand1 + operand2;
    },
    subtract: function (operand1, operand2) {
        return operand1 - operand2;
    },
    multiply: function (operand1, operand2) {
        return operand1 * operand2;
    },
    divide: function (operand1, operand2) {
        return operand1 / operand2;
    }
});